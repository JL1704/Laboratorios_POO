package personas;

/**
 *
 * @author josel
 */
public abstract class Persona {
    
    private String nombre;
    
    public Persona(String nombre) {
        super();
        this.nombre = nombre;
    }
    
    public abstract int run();

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
